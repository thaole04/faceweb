# This example is for use on (Linux) computers that are using CPython with
# Adafruit Blinka to support CircuitPython libraries. CircuitPython does
# not support PIL/pillow (python imaging library)!

import time

from board import SCL, SDA
import busio
from PIL import Image, ImageDraw, ImageFont
import adafruit_ssd1306


# Create the I2C interface.
i2c = busio.I2C(SCL, SDA)

# Create the SSD1306 OLED class.
# The first two parameters are the pixel width and pixel height.  Change these
# to the right size for your display!
disp = adafruit_ssd1306.SSD1306_I2C(128, 64, i2c)

# Clear display.
disp.fill(0)
disp.show()

# Create blank image for drawing.
# Make sure to create image with mode '1' for 1-bit color.
width = disp.width
height = disp.height
image = Image.new("1", (width, height))

# Get drawing object to draw on image.
draw = ImageDraw.Draw(image)

# Draw a black filled box to clear the image.
draw.rectangle((0, 0, width, height), outline=0, fill=0)

# Draw some shapes.
# First define some constants to allow easy resizing of shapes.
padding = -2
top = padding
bottom = height - padding
# Move left to right keeping track of the current x position for drawing shapes.
x = 0


# Load default font.
# font = ImageFont.load_default()

# Alternatively load a TTF font.  Make sure the .ttf font file is in the
# same directory as the python script!
# Some other nice fonts to try: http://www.dafont.com/bitmap.php
# font = ImageFont.truetype('/usr/share/fonts/truetype/dejavu/DejaVuSans.ttf', 9)
font = ImageFont.truetype('Roboto-Regular.ttf', 16)

def write(text, index):
    draw.text((x, top + index), text, font=font, fill=255)
    disp.image(image)
    disp.show()
    time.sleep(.1)
def clear():
    draw.rectangle((0,0,width,height), outline=0, fill=0)
    disp.image(image)
    disp.show()
    time.sleep(.1)
def manual(voice_enable):
    clear()
    write("Press button:", 0)
    write("1 Active system", 16)
    if voice_enable:
        write("2 Disable audio", 32)
    else:
        write("2 Enable audio", 32)
    write("3 Exit system", 48)
if __name__ == "__main__":
    manual(True)
    
